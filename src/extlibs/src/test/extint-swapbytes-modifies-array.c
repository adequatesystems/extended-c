
#include "_assert.h"
#include "../extint.h"

int main()
{  /* check swapbytes() swaps all bytes in array */
   unsigned char array[8] = { 0x1, 0x3, 0x7, 0xf, 0x1f, 0x3f, 0x7f, 0xff };
   unsigned char expect[8] = { 0xff, 0x7f, 0x3f, 0x1f, 0xf, 0x7, 0x3, 0x1 };
   swapbytes(array, 8); ASSERT_CMP(array, expect, 8);
}
